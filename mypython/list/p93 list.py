#EXTEND()
list1=[11,23,45,66,77,88,29,30]
list2=[33,44,55]

list1.extend(list2)
print(list1)


#SORT()
#order in line
list1=[11,23,45,66,77,88,29,30]
list1.sort()
print(list1)



#REVERSE
list1=[11,23,45,66,77,88,29,30]
list1.reverse()
print(list1)



#SORT & REVERSE
list1=[11,23,45,66,77,88,29,30]

