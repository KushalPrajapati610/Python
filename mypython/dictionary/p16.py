#14. Write a Python program to get the length of a dictionary.
# Sample data:
# marks = {"ram": 33,
#     "rahul": 45,
#     "devesh": 30,
#     "jayul": 34,
#     "jiya": 16}
# Expected Output:
# Length of dictionary: 5

marks = {"ram": 33,
    "rahul": 45,
    "devesh": 30,
    "jayul": 34,
    "jiya": 16}
print("Length of dictionary =",len(marks.items()))
