#6. Write a Python program to create a union of sets.

# Hint: Use the union() method or | operator to combine two sets.

# Sample data:
# s1 = {11, 22, 33, 44, 55}
# s2 = {11, 22, 99, 66, 55}
# Expected Output: {11, 22, 33, 44, 55, 99, 66}

s1 = {11, 22, 33, 44, 55}
s2 = {11, 22, 99, 66, 55}
print(s1.union(s2))
